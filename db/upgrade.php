/**
 * Function to upgrade auth_db.
 * @param int $oldversion the version we are upgrading from
 * @return bool result
 */
function xmldb_auth_telegram_upgrade($oldversion) {
    global $DB;
    $dbman = $DB->get_manager();
    if ($oldversion < 2023102509) {
        // Code to add the column, generated by the 'View PHP Code' option of the XMLDB editor.
        upgrade_plugin_savepoint(true, 2023102513, 'auth', 'telegram');
    }

    return true;
}
